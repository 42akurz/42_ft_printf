#include "ft_printf.h"

void	ft_putstr(char *s, int *count)
{
	int	i;

	if (!s)
	{
		write(1, "(null)", 6);
		*count = *count + 6;
		return ;
	}
	i = 0;
	while (s[i] != '\0')
	{
		ft_putchar(s[i], count);
		i++;
	}
}
